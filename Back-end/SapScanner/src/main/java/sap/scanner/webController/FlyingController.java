	private static final Logger logger = LoggerFactory.getLogger(FlyingController.class);

	@RequestMapping(value = "/flight-booking.do", method = RequestMethod.GET)
	public String booking(Locale locale, Model model) {
		logger.info("Welcome home! The client locale is {}.", locale);

		Date date = new Date();
		DateFormat dateFormat = DateFormat.getDateTimeInstance(DateFormat.LONG, DateFormat.LONG, locale);

		String formattedDate = dateFormat.format(date);

		model.addAttribute("serverTime", formattedDate);

		return "/flying/flight-booking";
	}	

